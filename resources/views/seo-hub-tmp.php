                    </p>

                    <h3 class="seo-hub__h3">Nearest Airport</h3>
                    <p class="seo-hub__para">
                        Jolly Grant Airport in Dehradun is the nearest airport, approximately 95 km
                        away. It has regular flights from Delhi. From the airport, the drive to
                        Kanatal takes approximately 2.5–3 hours.
